package step2;

public class Car {
	private String model;
	private int price;
	
	//setter
	public void setModel(String model) {
		this.model = model;
	}
	//getter
	public String getModel() {
		return model;
	}
	
	//setter
	public void setPrice(int price) {
		this.price = price;
	}
	//getter
	public int getPrice() {
		return price;
	}
}
