package step3;

public class TestVariable {
	 public static void main(String[] args) {
		 //int, 정수형 데이터 타입
		 // =, 할당 또는 대입
		 //2, data
		int num = 2; //메소드 내에서 선언된 변수이며, 지역변수.
		System.out.println(num);
		
		//역시 메서드 내에 선언된 지역변수
		//String, 문자열 데이터 타입
		//
		String name = "아이유";
		System.out.println(name);
	 }
}
